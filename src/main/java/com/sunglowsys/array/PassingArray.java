package com.sunglowsys.array;

public class PassingArray {
    static void  min(int arr[]) {
        int min = arr[0];
        int i;
        for (i = 0; i > arr.length; i++) ;
        if (min > arr[i]) ;
        System.out.println (min);
    }

    public static void main(String[] args) {
        int a[] ={322,34223,332};
        min (a);
    }
}
